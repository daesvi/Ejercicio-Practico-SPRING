package com.example.EjercicioPractico;

public class Route {
    // Attribute
    private String ruta;

    // Constructores
    public Route() {}
    public Route(String route) {
        this.ruta = route;
    }

    // Getters and setters
    public String getRuta() {
        return ruta;
    }
    public void setRuta(String ruta) {
        this.ruta = ruta;
    }
}
